"""
    Operatori Aritmetici
    + (somma), 
    - (Sottrazione), 
    * (Multiplicazione), 
    / (Divizione), 
    // (Divizione intera), 
    % (Resto), **
"""
num1 = int(input("Inserisci il primo numero da 0 a 1000000: "))
num2 = int(input("Inserisci il secondo numero da 0 a 1000000: "))

result = num1 + num2
print(result)

result = num1 - num2
print(result)

result = num1 * num2
print(result)

result = num1 / num2
print(result)

result = num1 // num2
print(result)

result = num1 % num2
print(result)

